import { readFile, mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const configPath = path.join(projectRoot, 'config', 'api-errors.config.json');

function readDefinition(code, value) {
  if (!/^[a-z][a-z0-9]*(?:[.-][a-z0-9]+)*$/u.test(code)) {
    throw new Error(`Invalid API error code: ${code}`);
  }
  if (!value || typeof value !== 'object' || Array.isArray(value)) {
    throw new Error(`${code} must be an object.`);
  }
  if (!Number.isInteger(value.status) || value.status < 400 || value.status > 599) {
    throw new Error(`${code}.status must be an HTTP error status.`);
  }
  if (typeof value.messageKey !== 'string' || !/^[a-z][A-Za-z0-9]*(?:\.[a-z][A-Za-z0-9]*)+$/u.test(value.messageKey)) {
    throw new Error(`${code}.messageKey must be a semantic locale key.`);
  }
  return { status: value.status, messageKey: value.messageKey };
}

function render(definitions) {
  return `// This file is generated automatically by scripts/generate-api-errors.mjs.
// Do not edit this file manually.

export const API_ERRORS = ${JSON.stringify(definitions, null, 2)} as const;

export type ApiErrorCode = keyof typeof API_ERRORS;
export type ApiErrorMessageKey = (typeof API_ERRORS)[ApiErrorCode]['messageKey'];

export function isApiErrorCode(value: unknown): value is ApiErrorCode {
  return typeof value === 'string' && Object.hasOwn(API_ERRORS, value);
}
`;
}

try {
  const raw = JSON.parse(await readFile(configPath, 'utf8'));
  if (!raw || typeof raw !== 'object' || Array.isArray(raw)) throw new Error('API errors config must be an object.');
  const definitions = Object.fromEntries(Object.entries(raw).map(([code, value]) => [code, readDefinition(code, value)]));
  if (!definitions['internal-error']) throw new Error('API errors config must define internal-error.');
  const output = render(definitions);
  const outputPaths = [
    path.join(projectRoot, 'src', 'generated', 'api-errors.ts'),
    path.join(projectRoot, 'supabase', 'functions', '_shared', 'api-errors.ts'),
    path.join(projectRoot, 'cloudflare', 'generated', 'api-errors.ts'),
  ];
  await Promise.all(outputPaths.map(async (outputPath) => {
    await mkdir(path.dirname(outputPath), { recursive: true });
    await writeFile(outputPath, output, 'utf8');
  }));
  console.info('Generated API error contract.');
} catch (error) {
  console.error('Failed to generate API error contract:', error instanceof Error ? error.message : error);
  process.exit(1);
}
